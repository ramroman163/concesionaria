    private void btnDeleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnDeleteMouseClicked
        if (table.getRowCount() > 0 && table.getSelectedRow() != -1) {
            // Obtenga el ID de la fila seleccionada
            int num_auto = Integer.parseInt(String.valueOf(table.getValueAt(table.getSelectedRow(), 0)));
            
            control.eliminar(num_auto);
            
            mostrarMensaje("Borrado exitoso", "info", "Borrado de automÃ³vil");
            
            cargarTabla();
                                    
        } else if(table.getSelectedRow() == -1){
            mostrarMensaje("No se ha seleccionado ningun automÃ³vil", "error", "Baja de automÃ³vil");
        } else if(table.getRowCount() <= 0){
            mostrarMensaje("No hay ningun automÃ³vil para eliminar", "error", "Baja de automÃ³vil");
        }
    }//GEN-LAST:event_btnDeleteMouseClicked

    private void btnEditMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEditMouseClicked
        if (table.getRowCount() > 0 && table.getSelectedRow() != -1) {
            // Obtenga el ID de la fila seleccionada
            int num_auto = Integer.parseInt(String.valueOf(table.getValueAt(table.getSelectedRow(), 0)));
            
            Edicion edicion = new Edicion(num_auto);
            
            edicion.setVisible(true);
            edicion.setLocationRelativeTo(null);
            
            dispose();
                                    
        } else if(table.getSelectedRow() == -1){
            mostrarMensaje("No se ha seleccionado ningun automÃ³vil", "error", "EdiciÃ³n de automÃ³vil");
        } else if(table.getRowCount() <= 0){
            mostrarMensaje("No hay ningun automÃ³vil para editar", "error", "EdiciÃ³n de automÃ³vil");
        }
    }//GEN-LAST:event_btnEditMouseClicked

    private void cargarTabla() {
        DefaultTableModel tableModel = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };

        String rowsTitle[] = {"Id", "Modelo", "Marca", "Motor", "Color", "Patente", "Puertas"};

        tableModel.setColumnIdentifiers(rowsTitle);

        List<Automovil> listaAutomoviles = control.traerAutos();

        if (listaAutomoviles != null) {
            for (Automovil automovil : listaAutomoviles) {
                Object filaAuto[] = {
                    automovil.getId(),
                    automovil.getModelo(),
                    automovil.getMarca(),
                    automovil.getMotor(),
                    automovil.getColor(),
                    automovil.getPatente(),
                    automovil.getCantidadPuertas()
                };

                tableModel.addRow(filaAuto);
            }

        }

        table.setModel(tableModel);
    }

    private void mostrarMensaje(String msj, String tipo, String titulo){
        JOptionPane optionPane = new JOptionPane(msj);
        if(tipo.equals("info")){
            optionPane.setMessageType(JOptionPane.INFORMATION_MESSAGE);
        } else if(tipo.equals("error")){
            optionPane.setMessageType(JOptionPane.ERROR_MESSAGE);
        }
        JDialog dialog = optionPane.createDialog(titulo);
        dialog.setAlwaysOnTop(true);
        dialog.setVisible(true);
    }
    
